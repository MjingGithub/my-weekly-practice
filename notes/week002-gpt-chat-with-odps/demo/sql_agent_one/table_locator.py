    "    {{\"table_name\": \"表名2\", \"reason\": \"使用原因2\"}}\n",
    "]\n",
    "\"\"\"\n",
    "\n",
    "        # 调用大模型分析\n",
    "        response = self.openai_client.chat.completions.create(\n",
    "            model=\"gpt-4o-mini\",\n",
    "            messages=[\n",
    "                {\"role\": \"system\", \"content\": \"你是一个专业的数据分析师，负责分析用户问题需要用到哪些表。\"},\n",
    "                {\"role\": \"user\", \"content\": prompt}\n",
    "            ],\n",
    "            temperature=0.1\n",
    "        )\n",
    "        \n",
    "        # 解析响应\n",
    "        try:\n",
    "            tables = eval(response.choices[0].message.content.strip())\n",
