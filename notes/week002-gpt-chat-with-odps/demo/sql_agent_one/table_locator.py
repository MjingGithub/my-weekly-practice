    "from typing import List, Dict\n",
    "import openai\n",
    "from dotenv import load_dotenv\n",
    "import os\n",
    "\n",
    "load_dotenv()\n",
    "\n",
    "class TableLocator:\n",
    "    def __init__(self):\n",
    "        self.openai_client = openai.OpenAI(api_key=os.getenv(\"OPENAI_API_KEY\"))\n",
    "        self.table_metadata = {}  # 存储表的元数据信息\n",
    "        \n",
    "    def load_table_metadata(self, table_metadata: Dict[str, Dict]):\n",
    "        \"\"\"加载表的元数据信息\"\"\"\n",
    "        self.table_metadata = table_metadata\n",
    "        \n",
    "    def _get_table_partition_info(self, table_name: str) -> Dict:\n",
    "        \"\"\"\n",
    "        获取表的分区信息\n",
    "        :param table_name: 表名\n",
    "        :return: 分区信息\n",
    "        \"\"\"\n",
    "        if table_name not in self.table_metadata:\n",
    "            raise ValueError(f\"未找到表 {table_name} 的元数据信息\")\n",
    "            \n",
    "        table_info = self.table_metadata[table_name]\n",
    "        return {\n",
    "            \"table_name\": table_name,\n",
    "            \"project_name\": table_info[\"project_name\"],\n",
    "            \"table_type\": table_info[\"table_type\"],\n",
    "            \"is_partitioned\":table_info[\"is_partitioned\"],\n",
    "            \"partition_column\": table_info[\"partition_column\"],\n",
    "            \"partition_type\": table_info[\"partition_type\"],\n",
    "            \"columns\": table_info[\"columns\"]\n",
    "        }\n",
    "        \n",
    "    def locate_tables(self, question: str) -> List[Dict]:\n",
    "        \"\"\"\n",
    "        根据用户问题定位相关表\n",
    "        :param question: 用户问题\n",
    "        :return: 相关表信息列表\n",
    "        \"\"\"\n",
    "        # 构建提示信息\n",
    "        table_info = []\n",
    "        for table_name, info in self.table_metadata.items():\n",
    "            project_name = info[\"project_name\"]\n",
    "            table_type = info[\"table_type\"]\n",
    "            partition_info = {\n",
    "                \"is_partitioned\": info[\"is_partitioned\"],\n",
    "                \"partition_column\": info[\"partition_column\"],\n",
    "                \"partition_type\": info[\"partition_type\"]\n",
    "            }\n",
    "            columns = info[\"columns\"]\n",
    "            \n",
    "            # 构建字段信息\n",
    "            columns_info = \"\\n\".join([\n",
    "                f\"  - {col['name']} ({col['type']})\" + \n",
    "                (f\" - {col['comment']}\" if col.get('comment') else \"\")\n",
    "                for col in columns\n",
    "            ])\n",
    "            \n",
    "            table_info.append(f\"\"\"\n",
    "表名: {table_name}\n",
    "类型: {table_type}\n",
    "分区信息: {partition_info}\n",
    "字段列表:\n",
    "{columns_info}\n",
    "\"\"\")\n",
    "            \n",
    "        prompt = f\"\"\"请根据以下信息分析用户问题需要用到哪些表：\n",
    "\n",
    "用户问题: {question}\n",
    "\n",
    "可用的表信息:\n",
    "{\"\".join(table_info)}\n",
    "\n",
    "请列出问题中涉及到的表名，并说明选择原因。\n",
